public class Stereo 
{
     public void turnStereoON()
     {
    	 System.out.println("Tun Stereo ON");
     }
     public void turnStereoOFF()
     {
    	 System.out.println("Turn Stereo OFF");
     }
     
     public void setCD()
     {
    	 System.out.println("Stereo is set: "+"for CD input");
     }
     public void setDVD()
     {
    	 System.out.println("Stereo is set: "+"for DVD input");
     }
}
